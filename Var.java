class Var
{
	public static void main(String args[])
	{
		int a=10,b=11;
		System.out.println(a + "and" + b);
		System.out.println("a&b: "+ (a&b));
		System.out.println("a|b: "+ (a|b));
		System.out.println("a^b: "+ (a^b));
		System.out.println("a>>2: "+ (a>>2));
		System.out.println("a<<2: "+ (a<<2));
	}
}